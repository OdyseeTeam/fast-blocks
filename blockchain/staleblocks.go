// GetStaleBlockHashes only works while lbrycrd is running, so run this first then shut it off to access leveldb
func GetStaleBlockHashes() ([]string, error) {
	client, err := lbrycrd.NewWithDefaultURL()
	if err != nil {
		return nil, err
	}

	staleBlocks := make(map[string]struct{})

	tips, err := client.GetChainTips()
	if err != nil {
		return nil, err
	}

	logrus.Printf("got %d chain tips", len(tips))

	for _, tip := range tips {
		if tip.Status != "valid-headers" || tip.BranchLen > 1000 {
			continue
		}

		logrus.Printf("doing tip %s with %d blocks", tip.Hash, tip.BranchLen)

		currHash, err := chainhash.NewHashFromStr(tip.Hash)
		if err != nil {
			return nil, err
		}

		staleBlocks[currHash.String()] = struct{}{}

		for i := 1; i < int(tip.BranchLen); i++ {
			block, err := client.GetBlock(currHash)
			if err != nil {
				return nil, err
			}

			currHash = &block.Header.PrevBlock

			logrus.Printf("moving backwards to %s", currHash)
			staleBlocks[currHash.String()] = struct{}{}
		}
	}

	staleHashes := make([]string, len(staleBlocks))
	i := 0
	for k := range staleBlocks {
		staleHashes[i] = k
		i++
	}

	return staleHashes, err
}

